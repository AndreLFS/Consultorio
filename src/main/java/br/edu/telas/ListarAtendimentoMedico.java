    private void atualizarTabela(){
        AtendimentoDAO atendimentoDAO = new AtendimentoDAO();
        List<Atendimento> lista = atendimentoDAO.listarHoje(valor, tipo);
        AtendimentoTableModel atendimentoTableModel = new AtendimentoTableModel(lista);
        jTable1.setModel(atendimentoTableModel);
    }
    private void atualizarTabela(String valor2, Object tipo2){
        AtendimentoDAO atendimentoDAO = new AtendimentoDAO();
        List<Atendimento> lista = atendimentoDAO.listarDoisCampos(valor, tipo, valor2, tipo2);
        AtendimentoTableModel atendimentoTableModel = new AtendimentoTableModel(lista);
        jTable1.setModel(atendimentoTableModel);
    }
    private void atualizarTabela(String valor2, Object tipo2,String valor3, Object tipo3){
        AtendimentoDAO atendimentoDAO = new AtendimentoDAO();
        List<Atendimento> lista = atendimentoDAO.listarTresCampos(valor, tipo, valor2, tipo2, valor3, tipo3);
        AtendimentoTableModel atendimentoTableModel = new AtendimentoTableModel(lista);
        jTable1.setModel(atendimentoTableModel);
    }
    private void passarClientes(){
        List<Cliente> clientes= clienteDAO2.listar();
        jC_clientes.removeAllItems();
        jC_idCliente.removeAllItems();
        jC_idCliente.setVisible(false);
        for (int i = 0; i < clientes.size(); i++) {
            jC_clientes.addItem(clientes.get(i).getNome());
            jC_idCliente.addItem(String.valueOf(clientes.get(i).getId()));
        }
    }
    
    private Atendimento getAtendimento(){
        int linha = jTable1.getSelectedRow();
        if (linha >= 0) {
            AtendimentoTableModel atendimentoTableModel = (AtendimentoTableModel) jTable1.getModel();
            Atendimento atendimento = atendimentoTableModel.getValueAt(linha);
            return atendimento;
        }else{
            JOptionPane.showMessageDialog(null, "Nenhuma Consulta selecionado");
            return null;
        }
    }
    
